import Navbar from "./components/Layout/Navbar";
import Pricing from "./components/Pricing/Pricing";
import { Routes, Route } from "react-router-dom";
import HomePage from "./components/Home/HomePage";
// import { ParallaxScroll } from "./components/ui/";
import Login from "./components/Auth/Login";
import Register from "./components/Auth/Register";
import { Toaster } from "react-hot-toast";
import RequestPasswordReset from "./components/Auth/RequestPasswordReset";
import ResetPassword from "./components/Auth/ResetPassword";
import GenerateImage from "./components/Home/GenerateImage";
import FAQ from "./components/Layout/FAQ";
import NotFound from "./components/Layout/NotFound";
import { useEffect } from "react";

import { Context } from "./main";
import { useContext } from "react";
import Dashboard from "./components/dashboard/Dashboard";
import FeatureDocumentation from "./components/Docs/FeatureDocumentation";
import BackgroundRemover from "./components/Remove/BackgroundRemover";

function App() {
  const { isAuthorized, setIsAuthorized, user, setUser } = useContext(Context);
  useEffect(() => {
    const fetchUser = async () => {
      try {
        const response = await axios.get(
          "http://localhost:3000/api/auth/user",
          {
            withCredentials: true, 
          }
        );
        setUser(response.data.user);
        setIsAuthorized(true);
      } catch (error) {
        setIsAuthorized(false);
      }
    };
    fetchUser();
  }, [isAuthorized]);

  return (
    <div>
      <Navbar />

      <Routes>
        <Route path={"/"} element={<HomePage />} />
        <Route path={"/login"} element={<Login />} />
        <Route path={"/register"} element={<Register />} />
        {/* <Route
          path={"/Examples"}
          element={<ParallaxScroll images={images} />}
        /> */}
        <Route path={"/Pricing"} element={<Pricing />} />
        <Route path={"/faq"} element={<FAQ />} />
        <Route path="/password-reset" element={<RequestPasswordReset />} />
        <Route path="/reset-password/:token" element={<ResetPassword />} />
        {/* to test gen ai api */}
        <Route path="/demo" element={<GenerateImage />} />
        <Route path="/docs" element={<FeatureDocumentation/>} />
        <Route path = "/features/remove-Bg" element = {<BackgroundRemover/>}/>
        <Route path="/dashboard" element={<Dashboard/>}/>
        <Route path="*" element={<NotFound />} /> 
      </Routes>
      <Toaster />
    </div>
  );
}

export default App;

